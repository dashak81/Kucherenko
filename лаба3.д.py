#Задание №1
n = int(input("Введите количество элементов списка -> "))
x = []

# Ввод списка
for i in range(n):
    x.append(int(input(f"Введите элемент {i} -> ")))

# Поиск наибольшего элемента, который делится на 2
max_even = None
for num in x:
    if num % 2 == 0:  # Проверка на четность
        if max_even is None or num > max_even:
            max_even = num

# Вывод результата
if max_even is not None:
    print("Наибольший четный элемент списка:", max_even)
else:
    print("Четных элементов в списке нет.")
#Задание №2
n = int(input("Введите количество элементов списка -> "))
x = []
y = []

# Ввод списка
for i in range(n):
    x.append(int(input(f"Введите элемент {i} -> ")))

# Формирование нового списка
for num in x:
    if num % 2 == 0 and num < 10:  # Четные числа, меньшие 10
        y.append(num)

# Сортировка нового списка
y.sort()

# Вывод результата
if y:
    print("Список четных чисел, меньших 10, в порядке возрастания:", y)
else:
    print("Четных чисел, меньших 10, в списке нет.")